/* Na formação de um While se tem:
   * contador (var que controla a repetição)
   * expressão (que determina hora de parar)
   * incremento
*/
 let contador = 1;
 while (contador < 6){
     console.log(`n° ${contador} com while.`)
     contador++;
 }
 console.log('______________________________________________');

 for(let i = 1; i < 6; i++){
     console.log(`n° ${i} com for.`)
 }